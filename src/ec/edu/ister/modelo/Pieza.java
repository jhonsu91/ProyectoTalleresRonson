package ec.edu.ister.modelo;

/**
 *
 * @author suntaxi-chasipanta
 */
public class Pieza {

    private String codigoPieza;
    private String nombrePieza;
    private String modeloPieza;
    private String colorPieza;
    private Double costoPieza;
    private int cantidadPieza;
    private String descripcionPieza;

    /**
     * @return the codigoPieza
     */
    public String getCodigoPieza() {
        return codigoPieza;
    }

    /**
     * @param codigoPieza the codigoPieza to set
     */
    public void setCodigoPieza(String codigoPieza) {
        this.codigoPieza = codigoPieza;
    }

    /**
     * @return the nombrePieza
     */
    public String getNombrePieza() {
        return nombrePieza;
    }

    /**
     * @param nombrePieza the nombrePieza to set
     */
    public void setNombrePieza(String nombrePieza) {
        this.nombrePieza = nombrePieza;
    }

    /**
     * @return the modeloPieza
     */
    public String getModeloPieza() {
        return modeloPieza;
    }

    /**
     * @param modeloPieza the modeloPieza to set
     */
    public void setModeloPieza(String modeloPieza) {
        this.modeloPieza = modeloPieza;
    }

    /**
     * @return the colorPieza
     */
    public String getColorPieza() {
        return colorPieza;
    }

    /**
     * @param colorPieza the colorPieza to set
     */
    public void setColorPieza(String colorPieza) {
        this.colorPieza = colorPieza;
    }

    /**
     * @return the costoPieza
     */
    public Double getCostoPieza() {
        return costoPieza;
    }

    /**
     * @param costoPieza the costoPieza to set
     */
    public void setCostoPieza(Double costoPieza) {
        this.costoPieza = costoPieza;
    }

    /**
     * @return the cantidadPieza
     */
    public int getCantidadPieza() {
        return cantidadPieza;
    }

    /**
     * @param cantidadPieza the cantidadPieza to set
     */
    public void setCantidadPieza(int cantidadPieza) {
        this.cantidadPieza = cantidadPieza;
    }

    /**
     * @return the descripcionPieza
     */
    public String getDescripcionPieza() {
        return descripcionPieza;
    }

    /**
     * @param descripcionPieza the descripcionPieza to set
     */
    public void setDescripcionPieza(String descripcionPieza) {
        this.descripcionPieza = descripcionPieza;
    }

}
